interface ListRowProps {
  inst: Instance
  selected: boolean
  onSelect: () => void
  /** True when this row is a sibling under the same namespace (not the first one shown). */
  isChild?: boolean
  /** Show the namespace label (only on the first row of a namespace group). */
  showNamespace?: boolean
}

function ListRow({ inst, selected, onSelect, isChild, showNamespace }: ListRowProps): React.JSX.Element {
  const nsLabel = inst.namespace || '(no namespace)'
  const isAuto = isAutoNamespace(inst.namespace)
  const nsTooltip = isAuto
    ? AUTO_NS_TOOLTIP
    : inst.namespace
      ? `Bus channel — host and remote use the same namespace to exchange events.`
      : `No namespace passed by the host. The package generated an internal id for the bus channel.`

  // Flash the row briefly when a new event arrives on this instance, so the
  // user can see at a glance which one is "active right now".
  const flashing = useFlashOnEvent(inst.events.length, inst.events[inst.events.length - 1]?.ts)

  return (
    <div
      className={`list-item${selected ? ' selected' : ''}${isChild ? ' child' : ''}${flashing ? ' flash' : ''}`}
      onClick={onSelect}
    >
      {showNamespace && (
        <div className="ns" title={nsTooltip}>
          {nsLabel}
          {isAuto && <span className="ns-auto" title={AUTO_NS_TOOLTIP}> (auto)</span>}
        </div>
      )}
      <div className="meta">
        <span
          className={`badge badge-${inst.mode === 'fetch' ? 'ssr-url' : inst.mode}`}
          title={MODE_TOOLTIPS[inst.mode] ?? inst.mode}
        >
          {inst.mode}
        </span>
        {inst.status === 'unmounted' && (
          <span className="badge badge-unmounted" title={STATUS_TOOLTIPS.unmounted}>unmounted</span>
        )}
        {inst.status === 'error' && (
          <span className="badge badge-error" title={STATUS_TOOLTIPS.error}>error</span>
        )}
        {inst.shadowDom && (
          <span className="badge badge-bridge" title="Rendered inside Shadow DOM (CSS-isolated)">shadow</span>
        )}
        <span title={`Instance id: ${inst.id}`}>{inst.id}</span>
      </div>
      {inst.url && (
        <div className="meta" title={inst.url}>
          {truncate(inst.url, 40)}
        </div>
      )}
    </div>
  )
}

interface NamespaceGroup {
  namespace: string
  instances: Instance[]
}

/**
 * Group instances by namespace so a paired LAZY+BRIDGE (the same MFBridgeLazy
 * spawning an inner MFBridge) shows up as a single visual group rather than
 * two unrelated rows. Within a group, sort by mode priority so LAZY (the
 * outer wrapper) renders first, with the resolved BRIDGE underneath it.
 */
function groupByNamespace(items: Instance[]): NamespaceGroup[] {
  const orderedNs: string[] = []
  const buckets = new Map<string, Instance[]>()
  for (const inst of items) {
    const key = inst.namespace || `__nons__:${inst.id}` // fetch and unnamed instances each in their own group
    if (!buckets.has(key)) {
      buckets.set(key, [])
      orderedNs.push(key)
    }
    buckets.get(key)!.push(inst)
  }
  // Sort within each group: lazy → bridge → hydrated → ssr-* → fetch → others.
  const modePriority: Record<string, number> = {
    lazy: 0, bridge: 1, hydrated: 2,
    'ssr-url': 3, 'ssr-loader': 4, fetch: 5,
  }
  for (const arr of buckets.values()) {
    arr.sort((a, b) => (modePriority[a.mode] ?? 99) - (modePriority[b.mode] ?? 99))
  }
  return orderedNs.map((key) => ({
    namespace: key.startsWith('__nons__:') ? '(no namespace)' : key,
    instances: buckets.get(key)!,
  }))
}

const ALL_KINDS: ReadonlyArray<MFEvent['kind']> = ['mount', 'unmount', 'props', 'event', 'load', 'fetch']

function Detail({ inst }: { inst: Instance }): React.JSX.Element {
  const lifetime = useLifetime(inst)
  const eventBreakdown = useMemo(() => summarizeEventKinds(inst.events), [inst.events])
  const [enabledKinds, setEnabledKinds] = useState<Set<MFEvent['kind']>>(new Set(ALL_KINDS))
  const filteredEvents = useMemo(
    () => inst.events.filter((e) => enabledKinds.has(e.kind)),
    [inst.events, enabledKinds],
  )
  const toggleKind = (k: MFEvent['kind']) => {
    setEnabledKinds((prev) => {
      const next = new Set(prev)
      next.has(k) ? next.delete(k) : next.add(k)
      return next
    })
  }
  // Counts per kind for the filter chips — only show kinds that exist on this instance.
  const kindCounts = useMemo(() => {
    const c: Record<string, number> = {}
    for (const e of inst.events) c[e.kind] = (c[e.kind] ?? 0) + 1
    return c
  }, [inst.events])
  return (
    <>
      <h2>
        <span
          title={
            isAutoNamespace(inst.namespace)
              ? AUTO_NS_TOOLTIP
              : inst.namespace
                ? 'Bus channel for host↔remote events'
                : 'No namespace prop — internal id used'
          }
        >
          {inst.namespace || '(no namespace)'}
        </span>
        {isAutoNamespace(inst.namespace) && (
          <span className="ns-auto" title={AUTO_NS_TOOLTIP}> (auto)</span>
        )}
        {' '}
        <span
          className={`badge badge-${inst.mode === 'fetch' ? 'ssr-url' : inst.mode}`}
          title={MODE_TOOLTIPS[inst.mode] ?? inst.mode}
          style={{ marginLeft: 4, verticalAlign: 'middle' }}
        >
          {inst.mode}
        </span>
        <small style={{ color: '#888', fontWeight: 400, marginLeft: 8 }}>· {inst.id}</small>
        <small className="lifetime" title="Time since mount, or total lifetime if unmounted">
          {lifetime}
        </small>
      </h2>
      <h3 title="Snapshot of the instance — what mode it is, current status, mount/unmount timestamps, optional URL (for SSR), and Shadow DOM flag.">Info</h3>
      <pre>{JSON.stringify({
        pkg: inst.pkg,
        mode: inst.mode,
        status: inst.status,
        url: inst.url,
        shadowDom: inst.shadowDom,
        mountedAt: new Date(inst.mountedAt).toISOString(),
        unmountedAt: inst.unmountedAt ? new Date(inst.unmountedAt).toISOString() : undefined,
      }, null, 2)}</pre>
      {inst.lastProps !== undefined && (
        <>
          <h3 title="The most recent props the host passed to the remote — either at mount or via a propsChanged update.">Last props</h3>
          <pre>{safeStringify(inst.lastProps)}</pre>
        </>
      )}
      <h3>
        Event log ({filteredEvents.length}
        {filteredEvents.length !== inst.events.length && ` of ${inst.events.length}`})
        {/* Only show the per-kind summary in the header when there's more than
            one kind — otherwise it just repeats "(N)" in different words. */}
        {Object.keys(kindCounts).length > 1 && eventBreakdown && (
          <small className="event-breakdown" title="Events on this instance, broken down by kind">
            {' '}— {eventBreakdown}
          </small>
        )}
      </h3>
      {/* Always show filter chips for any kind that exists — keeps the UI
          consistent so users always see what kinds of events are present
          and can filter them when more arrive. */}
      {inst.events.length > 0 && (
        <div
          className="event-filter"
          title="Toggle event kinds in the log below. Useful for high-traffic instances — for example, hide `props` to focus on bus events."
        >
          {ALL_KINDS.filter((k) => kindCounts[k] > 0).map((k) => (
            <label
              key={k}
              className={`chip${enabledKinds.has(k) ? ' on' : ''}`}
              title={KIND_TOOLTIPS[k]}
            >
              <input
                type="checkbox"
                checked={enabledKinds.has(k)}
                onChange={() => toggleKind(k)}
              />
              {k} <span className="chip-count">{kindCounts[k]}</span>
            </label>
          ))}
        </div>
      )}
      <div>
        {filteredEvents.map((e, i) => {
          // We need the actual index in `inst.events` to compute previous props correctly.
          const realIndex = inst.events.indexOf(e)
          return (
            <EventRow
              key={i}
              ev={e}
              t0={inst.mountedAt}
              prevProps={e.kind === 'props' ? findPrevProps(inst.events, realIndex) : undefined}
            />
          )
        })}
      </div>
    </>
  )
}

function EventRow({
  ev, t0, prevProps,
}: { ev: MFEvent; t0: number; prevProps?: unknown }): React.JSX.Element {
  const dt = `+${(ev.ts - t0).toString().padStart(4, ' ')}ms`
  let summary: React.ReactNode
  let cls = ''
  switch (ev.kind) {
    case 'mount':
      summary = <span>{ev.mode} mount {ev.namespace ? `[${ev.namespace}]` : ''}</span>
      break
    case 'unmount':
      summary = <span>unmount</span>
      break
    case 'props': {
      // Show a small diff between the previous props snapshot and this one.
      // Three cases:
      //   - diff has entries → render PropsDiff
      //   - prev exists, diff empty → reference changed but values are equal
      //     (e.g. a fresh inline object/function with the same shape, common
      //     when host re-renders without memoising callbacks). Surface this
      //     so it doesn't look like the panel "lost" the change.
      //   - no prev (synthetic instance, no mount) → fall back to snapshot.
      const diff = prevProps !== undefined ? diffProps(prevProps, ev.props) : null
      if (diff && diff.length > 0) {
        summary = <PropsDiff parts={diff} />
      } else if (prevProps !== undefined && diff !== null) {
        summary = (
          <span
            className="props-noop"
            title="Host re-emitted props but every key is structurally equal to the previous snapshot. Usually means the host re-rendered with a fresh inline object or callback. Wrap callbacks in useCallback to avoid this churn."
          >
            re-emitted (reference change, no value diff) — <code>{summarizeProps(ev.props)}</code>
          </span>
        )
      } else {
        summary = <code>{summarizeProps(ev.props)}</code>
      }
      break
    }
    case 'event': {
      const out = ev.direction === 'host->remote'
      cls = out ? 'arrow-out' : 'arrow-in'
      summary = (
        <span>
          <span title={DIRECTION_TOOLTIPS[ev.direction]}>
            {out ? '→ remote' : '← host'}
          </span>{' '}
          <strong title={`Bus event type: ${ev.type}`}>{ev.type}</strong>{' '}
          {ev.payload !== undefined && <code>{summarizeProps(ev.payload)}</code>}
        </span>
      )
      break
    }
    case 'load':
      summary = (
        <span className={ev.phase === 'error' ? 'err' : ''}>
          load:{ev.phase}
          {ev.attempt !== undefined ? ` (attempt ${ev.attempt})` : ''}
          {ev.error ? ` — ${ev.error}` : ''}
        </span>
      )
      break
    case 'fetch':
      summary = (
        <span className={ev.phase === 'error' ? 'err' : ''}>
          fetch:{ev.phase}
          {ev.attempt !== undefined ? ` #${ev.attempt}` : ''}
          {ev.error ? ` — ${ev.error}` : ''}
        </span>
      )
      break
  }
  return (
    <div className="event-row">
      <span className="ts" title={`Captured at ${new Date(ev.ts).toISOString()}`}>{dt}</span>
      <span className="kind" title={KIND_TOOLTIPS[ev.kind]}>{ev.kind}</span>
      <span></span>
      <span className={cls}>{summary}</span>
    </div>
  )
}

function summarizeProps(p: unknown): string {
  const s = safeStringify(p)
  return s.length > 80 ? s.slice(0, 77) + '…' : s
}

/**
 * Finds the props value that was active immediately before the props event
 * at `index`. That's either the most recent earlier `props` event, or the
 * `props` carried by the `mount` event if the instance hasn't streamed any
 * updates yet. Returns undefined if neither exists (synthetic instance).
 */
function findPrevProps(events: MFEvent[], index: number): unknown {
  for (let i = index - 1; i >= 0; i--) {
    const e = events[i]
    if (e.kind === 'props') return e.props
    if (e.kind === 'mount') return e.props
  }
  return undefined
}

type DiffPart =
  | { op: 'add'; key: string; value: unknown }
  | { op: 'remove'; key: string; value: unknown }
  | { op: 'change'; key: string; from: unknown; to: unknown }

function diffProps(prev: unknown, next: unknown): DiffPart[] {
  // Bail if either side isn't a plain object — fall back to the full snapshot.
  if (!isPlainObject(prev) || !isPlainObject(next)) return []
  const parts: DiffPart[] = []
  const keys = new Set([...Object.keys(prev), ...Object.keys(next)])
  for (const key of keys) {
    const a = prev[key]
    const b = next[key]
    const inPrev = key in prev
    const inNext = key in next
    if (inPrev && !inNext) parts.push({ op: 'remove', key, value: a })
    else if (!inPrev && inNext) parts.push({ op: 'add', key, value: b })
    else if (!structuralEqual(a, b)) parts.push({ op: 'change', key, from: a, to: b })
  }
  return parts
}

function isPlainObject(v: unknown): v is Record<string, unknown> {
  return typeof v === 'object' && v !== null && !Array.isArray(v)
}

/** Cheap deep-equal good enough for serialised props — uses JSON. */
function structuralEqual(a: unknown, b: unknown): boolean {
  if (a === b) return true
  try { return JSON.stringify(a) === JSON.stringify(b) } catch { return false }
}

function PropsDiff({ parts }: { parts: DiffPart[] }): React.JSX.Element {
  return (
    <span className="props-diff">
      {parts.map((p, i) => (
        <span key={i} className={`diff-${p.op}`} title={DIFF_OP_TOOLTIPS[p.op]}>
          {i > 0 && ' '}
          <span className="diff-op">
            {p.op === 'add' ? '+' : p.op === 'remove' ? '−' : '~'}
          </span>
          {' '}
          <code>{p.key}</code>
          {p.op === 'change' ? (
            <>
              : <code>{summarizeValue(p.from)}</code> → <code>{summarizeValue(p.to)}</code>
            </>
          ) : (
            <>: <code>{summarizeValue(p.value)}</code></>
          )}
        </span>
      ))}
    </span>
  )
}

function summarizeValue(v: unknown): string {
  if (v === undefined) return 'undefined'
  if (v === null) return 'null'
  if (typeof v === 'string') return JSON.stringify(v).length > 30
    ? JSON.stringify(v).slice(0, 27) + '…"'
    : JSON.stringify(v)
  if (typeof v === 'number' || typeof v === 'boolean') return String(v)
  if (Array.isArray(v)) return `[${v.length}]`
  if (typeof v === 'string' && v.startsWith('[Function')) return v
  if (isPlainObject(v)) {
    const keys = Object.keys(v)
    return keys.length === 0 ? '{}' : `{${keys.length} keys}`
  }
  return String(v)
}

function safeStringify(v: unknown): string {
  try { return JSON.stringify(v, null, 2) } catch { return String(v) }
}

function truncate(s: string, n: number): string {
  return s.length <= n ? s : s.slice(0, n - 1) + '…'
}

/**
 * Toggles a `flashing` flag for ~600ms whenever the given key (event count
 * or last-event ts) changes — used to briefly highlight a list row when
 * a new event arrives on its instance.
 */
function useFlashOnEvent(eventCount: number, lastTs: number | undefined): boolean {
  const [flashing, setFlashing] = useState(false)
  const lastSeenRef = useState({ count: eventCount, ts: lastTs })[0]
  useEffect(() => {
    if (eventCount === lastSeenRef.count && lastTs === lastSeenRef.ts) return
    lastSeenRef.count = eventCount
    lastSeenRef.ts = lastTs
    setFlashing(true)
    const id = setTimeout(() => setFlashing(false), 600)
    return () => clearTimeout(id)
  }, [eventCount, lastTs, lastSeenRef])
  return flashing
}

/**
 * Live "alive 12s" / "was alive 1m 23s" string for an instance. Re-renders
 * once a second while the instance is mounted; a frozen value once unmounted.
 */
function useLifetime(inst: Instance): string {
  const [, tick] = useState(0)
  useEffect(() => {
    if (inst.status === 'unmounted') return
    const id = setInterval(() => tick((n) => n + 1), 1000)
    return () => clearInterval(id)
  }, [inst.id, inst.status])

  if (inst.unmountedAt) {
    return `was alive ${formatDuration(inst.unmountedAt - inst.mountedAt)}`
  }
  return `alive ${formatDuration(Date.now() - inst.mountedAt)}`
}

function formatDuration(ms: number): string {
  if (ms < 0) ms = 0
  if (ms < 1000) return `${ms}ms`
  const s = Math.floor(ms / 1000)
  if (s < 60) return `${s}s`
  const m = Math.floor(s / 60)
  const rs = s % 60
  return rs === 0 ? `${m}m` : `${m}m ${rs}s`
}

/**
 * Trigger a browser download of a JSON dump of the captured instances and
 * their event logs. Filename includes a timestamp so multiple exports don't
 * overwrite each other.
 */
function exportEventsJson(items: Instance[]): void {
  const payload = {
    exportedAt: new Date().toISOString(),
    instanceCount: items.length,
    instances: items.map((i) => ({
      id: i.id,
      pkg: i.pkg,
      namespace: i.namespace,
      mode: i.mode,
      status: i.status,
      url: i.url,
      shadowDom: i.shadowDom,
      mountedAt: new Date(i.mountedAt).toISOString(),
      unmountedAt: i.unmountedAt ? new Date(i.unmountedAt).toISOString() : undefined,
      lastProps: i.lastProps,
      events: i.events,
    })),
  }
  const blob = new Blob([JSON.stringify(payload, null, 2)], { type: 'application/json' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = `mf-devtools-events-${new Date().toISOString().replace(/[:.]/g, '-')}.json`
  document.body.appendChild(a)
  a.click()
  document.body.removeChild(a)
  setTimeout(() => URL.revokeObjectURL(url), 1000)
}

function summarizeEventKinds(events: MFEvent[]): string {
  if (events.length === 0) return ''
  const counts: Record<string, number> = {}
  for (const e of events) counts[e.kind] = (counts[e.kind] ?? 0) + 1
  // Show in protocol order so the line reads consistently.
  const order: Array<MFEvent['kind']> = ['mount', 'unmount', 'props', 'event', 'load', 'fetch']
  return order
    .filter((k) => counts[k])
    .map((k) => `${counts[k]} ${k}`)
    .join(' · ')
}

/**
 * While paused, buffer incoming events and drain them on resume so the panel
 * doesn't drop activity that happens during inspection.
 */
function useBufferedPause(
  paused: boolean,
  dispatch: React.Dispatch<Action>,
): { current: boolean; buffer: MFEvent[] } {
  const ref = useMemo(() => ({ current: paused, buffer: [] as MFEvent[] }), [])
  ref.current = paused
  useEffect(() => {
    if (!paused && ref.buffer.length > 0) {
      const flushed = ref.buffer
      ref.buffer = []
      dispatch({ type: 'events', events: flushed })
    }
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [paused])
  return ref
}

