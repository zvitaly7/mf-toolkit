export function App(): React.JSX.Element {
  const [model, dispatch] = useReducer(modelReducer, initialModel)
  const [selected, setSelected] = useState<string | null>(null)
  const [paused, setPaused] = useState(false)
  const [hideUnmounted, setHideUnmounted] = useState(true)
  const [connState, setConnState] = useState<'connected' | 'reconnecting' | 'disconnected'>('disconnected')
  const [helpOpen, setHelpOpen] = useState(false)
  const pausedRef = useBufferedPause(paused, dispatch)

  // Wire up the long-lived port to the background service worker. MV3 service
  // workers can be evicted after ~30s of inactivity even with a port open; in
  // that case the port disconnects and any subsequent events from the page
  // are dropped on the floor. Reconnect on disconnect with a small delay, and
  // re-pull the content-bridge buffer so we don't miss anything that arrived
  // while the worker was down. The reducer dedupes identical events.
  useEffect(() => {
    const tabId = chrome.devtools.inspectedWindow.tabId
    let port: chrome.runtime.Port | null = null
    let cancelled = false
    let reconnectTimer: ReturnType<typeof setTimeout> | null = null

    const onMessage = (msg: RuntimeMessage): void => {
      if (msg.type !== 'mf-events') return
      if (pausedRef.current) {
        pausedRef.buffer.push(...msg.events)
        return
      }
      dispatch({ type: 'events', events: msg.events })
    }

    const pullBuffered = (): void => {
      void chrome.tabs.sendMessage<RuntimeMessage, { events?: MFEvent[] }>(
        tabId,
        { type: 'mf-panel-ready', tabId },
      ).then((res) => {
        if (cancelled) return
        if (res?.events?.length) dispatch({ type: 'events', events: res.events })
      }).catch(() => {
        // No content script on this tab (chrome:// page, no permissions, etc.).
      })
    }

    const connect = (): void => {
      if (cancelled) return
      port = chrome.runtime.connect({ name: `mf-devtools:${tabId}` })
      port.onMessage.addListener(onMessage)
      setConnState('connected')
      port.onDisconnect.addListener(() => {
        port = null
        if (cancelled) return
        setConnState('reconnecting')
        // Worker likely went idle. Reconnect after a short backoff; the
        // service worker will respawn on the new connect call.
        reconnectTimer = setTimeout(connect, 500)
      })
      pullBuffered()
    }

    connect()

    return () => {
      cancelled = true
      if (reconnectTimer !== null) clearTimeout(reconnectTimer)
      port?.disconnect()
    }
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [])

  const items = useMemo(() => model.order.map((id) => model.byId[id]).filter(Boolean), [model])
  const visibleItems = useMemo(
    () => (hideUnmounted ? items.filter((i) => i.status !== 'unmounted') : items),
    [items, hideUnmounted],
  )
  const groups = useMemo(() => groupByNamespace(visibleItems), [visibleItems])
  const liveCount = items.filter((i) => i.status !== 'unmounted').length
  const totalCount = items.length
  const selectedInstance = selected ? model.byId[selected] : null

  return (
    <div className="layout">
      <div className="toolbar">
        <button
          onClick={() => {
            dispatch({ type: 'clear' })
            setSelected(null)
            const tabId = chrome.devtools.inspectedWindow.tabId
            void chrome.tabs.sendMessage(tabId, { type: 'mf-clear', tabId }).catch(() => {})
          }}
          title="Clear all captured instances and events. Re-mount components or reload the page to see them again."
        >
          Clear
        </button>
        <button
          onClick={() => setPaused((p) => !p)}
          title={paused
            ? 'Currently paused — incoming events are buffered and will be flushed on resume.'
            : 'Pause stops the panel from processing incoming events. The page keeps emitting; events queue up and arrive in order when you resume.'}
        >
          {paused ? 'Resume' : 'Pause'}
        </button>
        <button
          onClick={() => exportEventsJson(items)}
          disabled={items.length === 0}
          title="Download a JSON dump of every captured instance and its event log. Useful for bug reports."
        >
          Export
        </button>
        <label className="toggle" title="StrictMode in dev double-mounts every effect, so the list fills with twin UNMOUNTED entries. Hiding them shows only what's currently in the DOM.">
          <input
            type="checkbox"
            checked={hideUnmounted}
            onChange={(e) => setHideUnmounted(e.target.checked)}
          />
          Hide unmounted
        </label>
        <span
          className="status"
          title={hideUnmounted
            ? `${liveCount} live, ${totalCount - liveCount} hidden as unmounted (${totalCount} total).`
            : `${totalCount} total instances captured this session (Clear to reset).`}
        >
          <span
            className={`conn-dot conn-${connState}`}
            title={CONN_TOOLTIPS[connState]}
            aria-label={`Connection: ${connState}`}
          />
          {hideUnmounted
            ? `${liveCount} of ${totalCount} ${totalCount === 1 ? 'instance' : 'instances'}`
            : `${totalCount} ${totalCount === 1 ? 'instance' : 'instances'}`}
        </span>
        <button
          className="help-btn"
          onClick={() => setHelpOpen((o) => !o)}
          title="What do these badges and event kinds mean? Cheat sheet."
          aria-label="Help"
        >
          ?
        </button>
      </div>
      {helpOpen && <HelpPopover onClose={() => setHelpOpen(false)} />}

      <div className="list">
        {visibleItems.length === 0 ? (
          <div className="empty">
            {totalCount === 0 ? (
              <>
                <strong>No microfrontends detected yet.</strong>
                <br />
                Open a page that mounts <code>&lt;MFBridge&gt;</code>, <code>&lt;MFBridgeLazy&gt;</code>,
                <code> &lt;MFBridgeHydrated&gt;</code>, or <code>&lt;MFBridgeSSR&gt;</code>.
                <br /><br />
                Events fire only in development builds (the emitter is dead-code-eliminated in production).
              </>
            ) : (
              <>
                <strong>All instances are unmounted.</strong>
                <br />
                Uncheck "Hide unmounted" above to see history.
              </>
            )}
          </div>
        ) : (
          groups.map((group) => (
            <div key={group.namespace} className="group">
              {group.instances.map((inst, idx) => (
                <ListRow
                  key={inst.id}
                  inst={inst}
                  selected={inst.id === selected}
                  onSelect={() => setSelected(inst.id)}
                  isChild={idx > 0 && group.instances.length > 1}
                  showNamespace={idx === 0}
                />
              ))}
            </div>
          ))
        )}
      </div>

      <div className="detail">
        {selectedInstance ? (
          <Detail inst={selectedInstance} />
        ) : (
          <div className="empty-detail">Select an instance to inspect.</div>
        )}
      </div>
    </div>
  )
}

