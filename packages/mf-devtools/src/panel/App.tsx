// Namespaces shaped `mfbridge-N` / `mfssr-N` are auto-generated by the
// package when the host doesn't pass `namespace` explicitly. Mark them so
// they don't read like deliberate channel names.
const AUTO_NAMESPACE_RE = /^mf(bridge|ssr)-\d+$/

function isAutoNamespace(ns: string | undefined): boolean {
  return !!ns && AUTO_NAMESPACE_RE.test(ns)
}

